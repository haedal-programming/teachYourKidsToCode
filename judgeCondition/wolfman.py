day = eval(input("오늘이 몇일인지 입력하세요 (1-31): "))
if day >= 25:
    print("안녕하세요 시민 늑대인간입니다")
elif day >= 20:
    print("내가 무슨 짓을 한거야")
elif day >= 15:
    print("아우우우우우")
elif day >= 10:
    print("나쁜 생각이 들어")
elif day >= 5:
    print("고기가 먹고 싶군")
else:
    print("내 몸이 왜 이럴까")

